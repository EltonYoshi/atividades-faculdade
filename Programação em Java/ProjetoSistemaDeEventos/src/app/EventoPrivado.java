package app;

/*Essa classe possui a função de herdar e definir um valor para o atributo "categoria" da superclasse.
 * Nesse caso a categoria será definida como PRIVADA*/

public class EventoPrivado extends Evento {

	//Método construtor que define um valor ao atributo "categoria"
	public EventoPrivado() {
		super("privado");
		
	}
	
	//Retorna os atributos da superclasse junto com a categorização diferenciada, PRIVADA
	@Override
	public String toString() {
		return "Nome do Evento: " + getNome() + "\nEndereço: " + getEndereco() + "\nCategoria: " + getCategoria() + "\nHorário de início: " +
	getHorarioInicioFormatado() + "\nHorário de saída: "+ getHorarioTerminoFormatado() + "\nDescrição: " + getDescricao();
	}
	


}
